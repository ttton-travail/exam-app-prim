// ===========================
// フリガナ辞書（小4準拠）
// lib/design/furigana.ts
//
// 「小学校で4年までに習わない漢字」を含む語にだけ、ふりがな（ruby）を付ける。
// <Furigana> コンポーネントが、表示テキスト中のこれらの語を見つけ、
// 「漢字部分にだけ」ruby を付ける（送り仮名・かなには付けない）。
//
// 採否の基準（学年別漢字配当表）：
//   ・含む漢字がすべて1〜4年配当 → フリガナ不要（辞書に入れない）
//     例：始める(始=3年) / 試す(試=4年) / 生成(生1・成4) / 特産品(特4・産4・品3) → 不要
//   ・5年以上 もしくは 小学校配当外(中学以降)の漢字を含む → フリガナ必要（辞書に入れる）
//     例：挑戦(挑=配当外) / 準備(準=5年) / 県庁(庁=6年) / 現在(現5・在5) など
//
// 値は「語全体の読み」でよい（コンポーネント側が漢字部分の読みを抽出して付ける）。
// 同音・文脈差のある語は、語ごとにこの辞書で読みを固定する。
//
// ※ フッター／プライバシーポリシーには適用しない（呼び出し側で制御）。
// ※ 都道府県名・特産品名など本文データ側の語は、別途この辞書に追加すれば
//    全画面に反映される（現状はUIラベルで必要な語のみ収録）。
// ===========================

export const FURIGANA: Record<string, string> = {
    // --- UIラベルで使う、5年以上/配当外の漢字を含む語 ---
    再挑戦: 'さいちょうせん',  // 再=5年, 挑=配当外
    挑戦: 'ちょうせん',        // 挑=配当外
    押す: 'おす',              // 押=配当外
    準備: 'じゅんび',          // 準=5年, 備=5年
    戻る: 'もどる',            // 戻=配当外
    項目: 'こうもく',          // 項=配当外
    現在: 'げんざい',          // 現=5年, 在=5年
    採点: 'さいてん',          // 採=5年
    未選択: 'みせんたく',      // 択=配当外
    選択: 'せんたく',          // 択=配当外

    // --- 項目名など（5年以上/配当外を含むもの） ---
    県庁所在地: 'けんちょうしょざいち', // 庁=6年（長い語を先に）
    県庁: 'けんちょう',                 // 庁=6年

    // 注：始める/試す/試して/生成/新た/特産品 等は 4年以下のみのため収録しない。
}

/** マッチ用に長さ降順で並べたキー一覧（長い語を優先して置換するため）。 */
export const FURIGANA_KEYS = Object.keys(FURIGANA).sort((a, b) => b.length - a.length)